#include "key.h"
#include "delay.h" 

//按键初始化函数
void KEY_Init(void)
{
	GPIO_InitTypeDef  GPIO_InitStructure;

  RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA|RCC_AHB1Periph_GPIOE, ENABLE);//使能GPIOA,GPIOE时钟
 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2|GPIO_Pin_3|GPIO_Pin_4|GPIO_Pin_5|GPIO_Pin_6|GPIO_Pin_7; //KEY0 KEY1 KEY2 KEY3等对应引脚
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN;//普通输入模式
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;//100M
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;//上拉
  GPIO_Init(GPIOE, &GPIO_InitStructure);//初始化GPIOE2,3,4,5,6,7
	
	 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0;//WK_UP对应引脚PA0
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_DOWN ;//下拉
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA0
 
} 
//按键处理函数
//返回按键值
//mode:0,不支持连续按;1,支持连续按;
//0，没有任何按键按下
//1，KEY0按下
//2，KEY1按下
//3，KEY2按下 
//4，WKUP按下 WK_UP
//注意此函数有响应优先级,KEY0>KEY1>KEY2>WK_UP!!
u8 KEY_Scan(u8 mode, u16 md)    
{	 
	static u8 key_up = 1;//按键按松开标志
	if(mode)key_up = 1;  //支持连按		  
	if(key_up&&(KEY0 == 0||KEY1 == 0||KEY2 == 0||KEY3 == 0||KEY4 == 0||KEY5 == 0||WK_UP==1))
	{
		delay_ms(10);//去抖动 
		key_up = 0;
		if(KEY0 == 0)return 1;
		else if(KEY1 == 0)return 2;
		else if(KEY2 == 0){if(md == 1 || md == 3 || md == 4){key_up = 1;delay_ms(150);}return 3;} //K2在模式1、3、4情况下可长按
		else if(WK_UP == 1)return 4;
		else if(KEY3 == 0)return 5;
		else if(KEY4 == 0)return 6;
		else if(KEY5 == 0){if(md == 1 || md == 3 || md == 4){key_up = 1;delay_ms(150);}return 7;} //K5同K2
	}else if(KEY0 == 1 && KEY1 == 1 && KEY2 == 1 && KEY3 == 1 && KEY4 == 1 && KEY5 == 1 && WK_UP == 0)key_up = 1; 	    
 	return 0;// 无按键按下
}

